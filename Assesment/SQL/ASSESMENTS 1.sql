SELECT DEPARTMENT, LAST_NAME, COUNT(*) AS count
FROM WORKER1
GROUP BY DEPARTMENT, LAST_NAME
HAVING COUNT > 1;

SELECT * FROM WORKER1 LIMIT 6;

SELECT DEPARTMENT, COUNT(*) AS TotalWorkers
FROM WORKER1
GROUP BY DEPARTMENT
HAVING COUNT(*) < 5;

SELECT Department, CONCAT(FIRST_NAME, ' ', LAST_NAME) AS EmployeeName, Salary
FROM WORKER1
WHERE (Department, Salary) IN (
  SELECT Department, MAX(Salary)
  FROM WORKER1
  GROUP BY Department
);



