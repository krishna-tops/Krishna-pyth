CREATE DATABASE ASSESMENTSQL ;
USE ASSESMENTSQL ;
CREATE TABLE WORKER1
(
 WORKER_ID INT PRIMARY KEY AUTO_INCREMENT,
 FIRST_NAME VARCHAR(40),
 LAST_NAME VARCHAR(40),
 SALARY INT ,
 JOINING_DATE DATETIME ,
 DEPARTMENT VARCHAR(35) 
) ;
